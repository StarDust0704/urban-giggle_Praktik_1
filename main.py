# Исходная строка, содержащая цифры и символы
my_string = 'ghybbi77 ajmdioeh 35kljnci88n 99'

# Создаем пустой список для хранения чисел
numbers = []

# Переменная для временного хранения цифр до их преобразования в число
temp = ''

# Проходимся по каждому символу в строке
for char in my_string:
    # Проверяем, является ли текущий символ цифрой
    if char.isdigit():
        # Если да, добавляем его к temp
        temp += char
    # Если текущий символ не цифра и temp не пустое,
    # значит мы нашли конец числа и можем добавить его в список
    elif temp != '':
        # Преобразуем temp в целое число и добавляем в список
        numbers.append(int(temp))
        # Очищаем temp для следующего числа
        temp = ''

# Если в конце строки остались цифры, добавляем их в список
if temp != '':
    numbers.append(int(temp))

# Выводим результат
print(numbers)